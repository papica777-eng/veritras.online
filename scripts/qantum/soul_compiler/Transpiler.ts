import * as fs from 'fs';
import * as exec from 'child_process';

const SOUL_SYNTAX_MAP = [
    { soul: /intention/g, rust: 'fn' },
    { soul: /sacred/g, rust: 'const' },
    { soul: /manifest/g, rust: 'let mut' },
    { soul: /eternal/g, rust: 'static' },
    { soul: /will/g, rust: 'return' },
    { soul: /entropy/g, rust: 'Err' },
    { soul: /nirvana/g, rust: 'Ok' },
    { soul: /-> karma<(.+)>/g, rust: '-> Result<$1, String>' } // Karma handling
];

export class SoulTranspiler {

    public static transpile(soulFilePath: string): string {
        console.log(`🔮 [SOUL] Breathing life into: ${soulFilePath}`);

        let soulCode = fs.readFileSync(soulFilePath, 'utf-8');
        let rustCode = soulCode;

        // 1. Превод на Лексикона (.soul -> .rs)
        SOUL_SYNTAX_MAP.forEach(rule => {
            rustCode = rustCode.replace(rule.soul, rule.rust as string);
        });

        // 2. Инжектиране на HEX подписа (DNA Injection)
        const hexHeader = `
// GENERATED BY SOUL COMPILER v1.0
// ORIGIN: QAntum_LOGOS (0x41455445524e415f4c4f474f535f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d495441525f50524f44524f44494d49544152)
// DO NOT MODIFY MANUALLY.
#![allow(unused_variables)]
use std::collections::HashMap;
`;
        return hexHeader + rustCode;
    }

    public static compileToBinary(rustCode: string, outputName: string) {
        // Записва временен .rs файл
        fs.writeFileSync(`${outputName}.rs`, rustCode);

        // Вика Cargo/Rustc за компилация до Native Code
        console.log(`🔥 [FORGE] Compiling ${outputName}.rs to High-Performance Binary...`);

        exec.exec(`rustc --crate-type cdylib ${outputName}.rs -o ${outputName}.so -O`, (err, stdout, stderr) => {
            if (err) {
                console.error(`❌ [ENTROPY] Compilation Failed:`, stderr);
            } else {
                console.log(`✅ [GENESIS] Binary Manifested: ${outputName}.so`);
            }
        });
    }
}
